    <p class="mb-4">Anda bisa menjual peralatan komputer di berbagai platform seperti marketplace online (Tokopedia, Shopee, Lazada), media sosial (Instagram, Facebook Marketplace), atau bahkan website pribadi. Pilih platform yang sesuai dengan target pasar Anda untuk menjangkau lebih banyak pembeli potensial.</p>

